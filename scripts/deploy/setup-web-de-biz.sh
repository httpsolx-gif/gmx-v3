# ============================================================
# WEB.DE на домене web-de.biz — пошаговые команды
# Выполнять на СЕРВЕРЕ (кроме шага 0). Подставь свой путь к проекту.
# ============================================================

# Путь к проекту на сервере (если другой — замени во всех командах ниже)
GMX_PATH="/var/www/gmx-net.help"

# ============================================================
# ШАГ 0. С локального Mac: залить свежий код (чтобы был config/nginx-web-de.biz.conf)
# ============================================================
# cd /Users/greedy/Desktop/gmx
# ./deploy/deploy-gmx-with-backup.sh
# или: DEPLOY_HOST=root@ТВОЙ_IP ./deploy/deploy-gmx-with-backup.sh

# ============================================================
# ШАГ 1. На сервере: добавить web-de.biz в список доменов WEB.DE
# ============================================================
# Подключись: ssh root@ТВОЙ_IP

# Открыть .env и добавить или заменить строку WEBDE_DOMAINS:
nano $GMX_PATH/.env

# В файле должна быть строка (остальное оставь как есть):
# WEBDE_DOMAINS=web-de.biz,www.web-de.biz
# Сохранить: Ctrl+O, Enter, Ctrl+X

# ============================================================
# ШАГ 2. Перезапустить приложение
# ============================================================
cd $GMX_PATH && npm install && pm2 reload ecosystem.config.cjs --only gmx-net --update-env && pm2 save

# ============================================================
# ШАГ 3. Подключить Nginx для web-de.biz
# ============================================================
cp $GMX_PATH/config/nginx-web-de.biz.conf /etc/nginx/sites-enabled/web-de.biz

# Если порт приложения не 3001 — отредактировать:
# nano /etc/nginx/sites-enabled/web-de.biz
# Заменить 3001 на свой PORT из .env в строках proxy_pass

# Проверить конфиг и перезагрузить Nginx:
nginx -t && systemctl reload nginx

# ============================================================
# ШАГ 4. Выдать SSL-сертификат (HTTPS) для web-de.biz
# ============================================================
certbot certonly --webroot -w /var/www/certbot -d web-de.biz -d www.web-de.biz

# Если спросит email — ввести. Согласиться с условиями (Y).

# ============================================================
# ШАГ 5. Включить HTTPS в Nginx
# ============================================================
nano /etc/nginx/sites-enabled/web-de.biz

# Раскомментировать весь второй блок server { ... } (строки с listen 443 ssl;
# ssl_certificate; location /; и т.д. — убрать # в начале строк).
# Сохранить: Ctrl+O, Enter, Ctrl+X.

nginx -t && systemctl reload nginx

